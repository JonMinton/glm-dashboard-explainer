def gaussian_report(n):
    x = exercise[:n]
    y = rest_hr[:n]
    X = sm.add_constant(x)

    # --- OLS via statsmodels (the convenience route) ---
    ols = sm.OLS(y, X).fit()

    # --- Full ML via scipy.optimize, JonStats-style ---
    # pars = (beta0, beta1, eta) with sigma^2 = exp(eta)
    def negll(pars):
        b0, b1, eta = pars
        s2 = np.exp(eta)
        mu = b0 + b1 * x
        return 0.5 * np.sum(np.log(2 * np.pi * s2) + (y - mu) ** 2 / s2)

    fit = optimize.minimize(negll, x0=np.array([0.0, 0.0, 0.0]), method="BFGS")

    # Numeric Hessian of the NEGATIVE log-likelihood by central differences
    def num_hessian(f, p, h=1e-4):
        k = len(p)
        H = np.zeros((k, k))
        for i in range(k):
            for j in range(k):
                pp = p.copy(); pp[i] += h; pp[j] += h
                pm = p.copy(); pm[i] += h; pm[j] -= h
                mp = p.copy(); mp[i] -= h; mp[j] += h
                mm = p.copy(); mm[i] -= h; mm[j] -= h
                H[i, j] = (f(pp) - f(pm) - f(mp) + f(mm)) / (4 * h * h)
        return H

    H = num_hessian(negll, fit.x)
    # negll is minimised, so H is the observed information directly
    vcov_ml = np.linalg.inv(H)
    se_ml = np.sqrt(np.diag(vcov_ml))

    sigma2_ml = np.exp(fit.x[2])

    print(f"\n--- n = {n} ---")
    print(f"OLS  coefficients: b0 = {ols.params[0]:.4f}, b1 = {ols.params[1]:.4f}")
    print(f"OLS  std errors:   se(b0) = {ols.bse[0]:.4f}, se(b1) = {ols.bse[1]:.4f}")
    print(f"OLS  sigma-hat (df-corrected): {np.sqrt(ols.mse_resid):.4f}")
    print(f"OLS  vcov(beta):\n{ols.cov_params()}")
    print(f"ML   coefficients: b0 = {fit.x[0]:.4f}, b1 = {fit.x[1]:.4f}")
    print(f"ML   sigma-hat (no df correction): {np.sqrt(sigma2_ml):.4f}")
    print(f"ML   std errors from inverse Hessian: se(b0) = {se_ml[0]:.4f}, "
          f"se(b1) = {se_ml[1]:.4f}, se(eta) = {se_ml[2]:.4f}")
    print(f"ML   vcov(beta) from inverse Hessian:\n{vcov_ml[:2, :2]}")
    print(f"Wald 95% CI b1 (OLS): "
          f"({ols.params[1] - 1.96 * ols.bse[1]:.4f}, {ols.params[1] + 1.96 * ols.bse[1]:.4f})")

    # Analytic profile Hessian at the MLE (sigma^2 fixed at ML estimate):
    # for the LOG-LIKELIHOOD (maximised), H = -X'X / sigma^2
    XtX = X.T @ X
    H_profile = -XtX / sigma2_ml
    print(f"Analytic profile Hessian of ll at peak (sigma^2 fixed at ML):\n{H_profile}")
    print(f"Inverse of negated profile Hessian (= vcov used by the page):\n"
          f"{np.linalg.inv(-H_profile)}")
    return ols


gaussian_report(DEFAULT_N)
gaussian_report(N_FULL)

