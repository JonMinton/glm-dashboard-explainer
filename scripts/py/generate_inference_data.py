def gauss_ml_ll(y, resid):
    """Gaussian max log-likelihood with sigma^2 at its ML value."""
    n = len(y)
    s2 = np.sum(resid ** 2) / n
    return -0.5 * n * (np.log(2 * np.pi * s2) + 1)


def gauss_lr_report(n):
    x, y = exercise[:n], rest_hr[:n]
    ols_full = sm.OLS(y, sm.add_constant(x)).fit()
    ll_full = gauss_ml_ll(y, ols_full.resid)
    ll_rest = gauss_ml_ll(y, y - y.mean())
    lam = 2 * (ll_full - ll_rest)
    print(f"  n={n:3d}: ll_full = {ll_full:.4f}, ll_mean-only = {ll_rest:.4f}, "
          f"Lambda = {lam:.4f}, chi2(1) p = {stats.chi2.sf(lam, 1):.3e}")
    # cross-check against statsmodels' own llf
    assert abs(ll_full - ols_full.llf) < 1e-8


print("Gaussian: full (b0 + b1 x) vs mean-only, both with ML sigma^2:")
