MODELS = [
    ("M0: intercept only", []),
    ("M1: + exercise", ["exercise"]),
    ("M2: + age", ["exercise", "age"]),
    ("M3: + shoe size (junk)", ["exercise", "age", "shoe"]),
]
cols = {"exercise": mc_exercise, "age": mc_age, "shoe": mc_shoe}


def mc_table(n):
    print(f"\n--- n = {n} (AIC/BIC in the R convention: k counts coefficients + sigma) ---")
    print(f"{'model':<24} {'k':>2} {'logLik':>10} {'AIC':>9} {'BIC':>9}")
    for name, preds in MODELS:
        X = np.column_stack([np.ones(n)] + [cols[c][:n] for c in preds])
        fit = sm.OLS(mc_hr[:n], X).fit()
        k = X.shape[1] + 1          # + sigma, matching R's AIC()/BIC()
        aic = -2 * fit.llf + 2 * k
        bic = -2 * fit.llf + np.log(n) * k
        print(f"{name:<24} {k:>2} {fit.llf:>10.4f} {aic:>9.4f} {bic:>9.4f}")
    full = sm.OLS(mc_hr[:n], np.column_stack(
        [np.ones(n), mc_exercise[:n], mc_age[:n], mc_shoe[:n]])).fit()
    print("full-model coefficients:", np.round(full.params, 4),
          " SEs:", np.round(full.bse, 4))


mc_table(N_MC)
