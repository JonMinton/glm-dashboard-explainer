"""
Generate and validate datasets + displayed numbers for the Statistical
Inference section (docs/inference/).

Two datasets, both deterministic (seed 42):

1. Resting heart rate vs weekly exercise hours (Gaussian linear model)
   - used on curvature.html for the 2D log-likelihood surface,
     Hessian -> vcov -> SE pipeline, and the R/Python code panels.
   - n = 200 generated; the page's sample-size slider uses the first n rows.

2. Small-n logistic dataset (treatment response vs biomarker score)
   - used on bayes.html to contrast Wald (quadratic) intervals with
     flat-prior grid-posterior credible intervals.

All numbers quoted in the HTML pages must match this script's output.
Run from scripts/py/:  python3 generate_inference_data.py
"""

import json
import numpy as np
import statsmodels.api as sm
from scipy import stats, optimize

rng = np.random.default_rng(42)

